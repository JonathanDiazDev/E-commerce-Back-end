-- -------------------------------------------------------------
-- ORDER_ITEMS
-- -------------------------------------------------------------
CREATE TABLE order_items (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    quantity          INTEGER        NOT NULL,
    price_at_purchase NUMERIC(38, 2) NOT NULL,
    order_id          BIGINT         NOT NULL,
    product_id        BIGINT         NOT NULL,

    CONSTRAINT fk_order_item_order   FOREIGN KEY (order_id)   REFERENCES orders (id),
    CONSTRAINT fk_order_item_product FOREIGN KEY (product_id) REFERENCES products (id)
);

-- -------------------------------------------------------------
-- PAYMENT_ATTEMPT
-- -------------------------------------------------------------
CREATE TABLE payment_attempt (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    payment_id     BIGINT,
    attempt_number INTEGER      NOT NULL,
    status         VARCHAR(255),
    error_message  VARCHAR(255),
    transaction_id VARCHAR(255),
    attempted_at   TIMESTAMP WITH TIME ZONE,

    CONSTRAINT payment_attempt_status_check CHECK (status IN ('SUCCESS', 'FAILED', 'PENDING')),
    CONSTRAINT fk_attempt_payment FOREIGN KEY (payment_id) REFERENCES payment (id)
);

-- -------------------------------------------------------------
-- REFRESH_TOKEN
-- (V3 convierte family_id de VARCHAR a UUID y añade user_agent, ip_address, created_at)
-- -------------------------------------------------------------
CREATE TABLE refresh_token (
    id                     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    token_hash             VARCHAR(255) NOT NULL,
    user_id                BIGINT,
    expires_at             TIMESTAMP WITH TIME ZONE,
    revoked                BOOLEAN      NOT NULL DEFAULT FALSE,
    replaced_by_token      VARCHAR(255),
    replaced_by_token_hash VARCHAR(255),
    family_id              VARCHAR(36),

    CONSTRAINT refresh_token_token_hash_key UNIQUE (token_hash),
    CONSTRAINT fk_refresh_token_user FOREIGN KEY (user_id) REFERENCES users (id)
);

-- -------------------------------------------------------------
-- FAILED_EMAILS
-- (product_name existía en la base real aunque no está en la entity actual)
-- (V7 añade email_type con IF NOT EXISTS — se incluye ya en el baseline)
-- -------------------------------------------------------------
CREATE TABLE failed_emails (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    recipient     VARCHAR(255) NOT NULL,
    product_name  VARCHAR(255) NOT NULL,
    email_type    VARCHAR(255) NOT NULL DEFAULT 'UNKNOWN',
    payload       TEXT,
    error_message TEXT,
    occurred_at   TIMESTAMP WITH TIME ZONE NOT NULL,
    retry_count   INTEGER,
    status        VARCHAR(255),

    CONSTRAINT failed_emails_status_check CHECK (status IN ('PENDING', 'RETRYING', 'SENT', 'FAILED'))
);

-- -------------------------------------------------------------
-- FAILED_PAYMENT_EVENTS
-- -------------------------------------------------------------
CREATE TABLE failed_payment_events (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id          BIGINT   NOT NULL,
    attempt_number    INTEGER  NOT NULL,
    error_message     TEXT,
    failed_at         TIMESTAMP WITH TIME ZONE NOT NULL,
    notification_sent BOOLEAN  NOT NULL DEFAULT FALSE
);

-- -------------------------------------------------------------
-- STOCK_NOTIFICATION
-- -------------------------------------------------------------
CREATE TABLE stock_notification (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT,
    product_id  BIGINT,
    created_at  TIMESTAMP WITH TIME ZONE,
    is_notified BOOLEAN NOT NULL DEFAULT FALSE,

    CONSTRAINT fk_stock_notif_user    FOREIGN KEY (user_id)    REFERENCES users (id),
    CONSTRAINT fk_stock_notif_product FOREIGN KEY (product_id) REFERENCES products (id)
);

